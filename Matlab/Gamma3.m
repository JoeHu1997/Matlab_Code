function G3 = Gamma3(n, m, k1, k2, sigma01, sigma02, g, h, Om);

Knm = Kappa3(n, k1, m, k2);
K11 = Kappa3(1, k1, 1, k2);
Snm = Sigma3(n, sigma01, m, sigma02);
S11 = Sigma3(1, sigma01, 1, sigma02);
G11 = Gamma2(k1, k2, sigma01, sigma02, g ,h, Om);
L11 = Lambda2(k1, k2, sigma01, sigma02, g, h, Om);
if n>m
    G2 = Gamma2(k1, k1, sigma01, sigma01, g, h, Om);
    L2 = Lambda2(k1, k1, sigma01, sigma01, g, h, Om);
elseif n<m %Only for waves that propagates +x
    G2 = Gamma2(k2, k2, sigma02, sigma02, g, h, Om);
    L2 = Lambda2(k2, k2, sigma02, sigma02, g, h, Om);
else
    G2 = 0;
    L2 = 0;
end

gamma = Knm*sinh(Knm*h)-Snm^2*cosh(Knm*h)/g-Om*Snm*sinh(Knm*h)/g;

G3 = (1/gamma)*(G11*K11*(-Knm*cosh(K11*h)+Snm*(Snm-S11)*...
    (sinh(K11*h)-coth((Knm-K11)*h)*cosh(K11*h))/g+Snm*S11*sinh(K11*h)/g)...
    +G2*(Knm-K11)*(-Knm*cosh(2*(Knm-K11)*h)+Snm*(2*S11-Snm)*...
    (sinh(2*(Knm-K11)*h)-coth((2*K11-Knm)*h)*cosh(2*(Knm-K11)*h))/g+...
    2*Snm*(Snm-S11)*sinh(2*(Knm-K11)*h)/g)...
    +L11*(Snm-S11)*(-Knm*coth((Knm-K11)*h)+Snm*(Snm-S11)/g)...
    +L2*(2*S11-Snm)*(-0.5*Knm*coth((2*K11-Knm)*h)+0.5*Snm*(2*S11-Snm)/g)...
    +Om*Knm*(-L11-0.5*L2)...
    +(-(1/4)*Knm*(2*(Knm-K11)*(Snm-S11)+(2*K11-Knm)*(2*S11-Snm))...
    +(1/2/g)*Snm*(Snm-S11)*(2*S11-Snm)*((Knm-K11)-(2*K11-Knm))*...
    (coth((Knm-K11)*h)-coth((2*K11-Knm)*h))...
    +(1/4/g)*Snm*(2*(Knm-K11)*(Snm-S11)^2*coth((Knm-K11)*h)+...
    (2*K11-Knm)*(2*S11-Snm)^2*coth((2*K11-Knm)*h))...
    -(1/4/g)*Snm*(2*(Knm-K11)*(Snm-S11)+(2*K11-Knm)*(2*S11-Snm))*Om));
end