gamma = (2*k1+k2)*sinh((2*k1+k2)*h)-(2*sigma01+sigma02)^2*cosh((2*k1+k2)*h)/g-Om*(2*sigma01+sigma02)*sinh((2*k1+k2)*h)/g;

G3 = (1/gamma)*(G11*(k1+k2)*(-(2*k1+k2)*cosh((k1+k2)*h)+(2*sigma01+sigma02)*sigma01*...
    (sinh((k1+k2)*h)-coth(k1*h)*cosh((k1+k2)*h))/g+(2*sigma01+sigma02)*(sigma01+sigma02)*sinh((k1+k2)*h)/g)...
    +G2*k1*(-(2*k1+k2)*cosh(2*k1*h)+(2*sigma01+sigma02)*sigma02*...
    (sinh(2*k1*h)-coth(k2*h)*cosh(2*k1*h))/g+...
    2*(2*sigma01+sigma02)*sigma01*sinh(2*k1*h)/g)...
    +L11*sigma01*(-(2*k1+k2)*coth(k1*h)+(2*sigma01+sigma02)*sigma01/g)...
    +L2*sigma02*(-0.5*(2*k1+k2)*coth(k2*h)+0.5*(2*sigma01+sigma02)*sigma02/g)...
    +Om*(2*k1+k2)*(-L11-0.5*L2)...
    +(-(1/4)*(2*k1+k2)*(2*k1*sigma01+k2*sigma02)...
    +(1/2/g)*(2*sigma01+sigma02)*sigma01*sigma02*(k1-k2)*...
    (coth(k1*h)-coth(k2*h))...
    +(1/4/g)*(2*sigma01+sigma02)*(2*k1*sigma01^2*coth(k1*h)+...
    k2*sigma02^2*coth(k2*h))...
    -(1/4/g)*(2*sigma01+sigma02)*(2*k1*sigma01+k2*sigma02)*Om));
end